     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $input = $request->all();
        $reciept = Reciept::all()->sortByDesc('created_at')->first();
        $input['number'] = $reciept->number + 1;
        selfReciepts::create($input);
        $request->session()->flash('created', 'Reciept generated successfully');
        return redirect()->back();
    }

